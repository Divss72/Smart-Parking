export function getAvailability(lot) {
  const available = lot.totalSlots - lot.occupied;
  const pct = (available / lot.totalSlots) * 100;
  if (pct > 30) return 'high';
  if (pct > 10) return 'medium';
  return 'low';
}

export function getAvailabilityColor(lot) {
  const avail = getAvailability(lot);
  if (avail === 'high') return '#00e676';
  if (avail === 'medium') return '#ffc107';
  return '#ff5252';
}
